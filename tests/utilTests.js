'use strict';

var proxyquire = require('proxyquireify')(require);
var Util = require('../src/util/util');

describe('Util', function() {

	describe('randomId', function() {

		it('generates a random id that looks like a guid', function() {
			var result = Util.randomId();
			expect(result).to.match(/^[A-Za-z0-9]{8}-[A-Za-z0-9]{4}-[A-Za-z0-9]{4}-[A-Za-z0-9]{4}-[A-Za-z0-9]{12}/);

		});

	});

});
